package overiding;

public class Demo_overriding {
    public void district_name(String district1,String district2)
    {
        if((district1==district2)||(district1!=district2))
        {
            System.out.println("My Favourite food is Briyani at Trichy");
        }
        else
        {
            System.out.println("My Favourite juices is Watermelon");
        }
    }
}
