package operator;

public class increanddecre {
    public static void main(String[] args) {
        //Pre increment
        int cosmo = 200;
        int alpha = 199;
        System.out.println(++cosmo);
        System.out.println(--alpha);
        //Post Increment
        System.out.println(cosmo++);
        System.out.println(alpha--);

    }
}