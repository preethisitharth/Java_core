package operator;

public class bitwise {
        public static void main(String[] args) {
            int rasagulla = 240;
            int blackforest = 340;
            System.out.println("Bitwise And:" + (rasagulla & blackforest));
            System.out.println("Bitwise OR:" + (rasagulla | blackforest));
            System.out.println("Right Shift of rasagulla by 2: " + (rasagulla >> 2));
            System.out.println("Left Shift of rasagulla by 2: " + (rasagulla << 2));
            System.out.println("Right Shift of blackforest by 2: " + (blackforest >> 2));
            System.out.println("Left Shift of blackforest by 2: " + (blackforest << 2));
                }
            }



